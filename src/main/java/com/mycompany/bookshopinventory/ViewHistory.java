    private void listData() {
        List<Purchase> purchases = getPurchase();
        for (Purchase purchase : purchases) {
            purchaseModel.addRow(new Object[]{purchase.getUsername(), purchase.getBookshopItem(), purchase.getQuantity(), purchase.getPrice()});
        }
    }

    public List<Purchase> getPurchase() {
        List<Purchase> purchases = new ArrayList<>();
        try {
            conn = DriverManager.getConnection(Constants.url, Constants.user, Constants.password);
            ps = conn.prepareStatement(DISPLAY_PURCHASES_SQL);
            ps.setString(1, username);
            rs = ps.executeQuery();
            while (rs.next()) {
                String username = rs.getString("username");
                String bookshopItem = rs.getString("bookshop_item");
                int quantity = rs.getInt("quantity");
                double price = rs.getDouble("price");
                Purchase purchase = new Purchase(username, quantity, bookshopItem, price);
                purchases.add(purchase);
            }
            rs.close();
            ps.close();
            conn.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        return purchases;
    }

