    private void listData() {
        try {
            conn = DriverManager.getConnection(Constants.url, Constants.user, Constants.password);
            ps = conn.prepareStatement(DISPLAY_STOCK_SQL);
            rs = ps.executeQuery();
            while (rs.next()) {
                String str = rs.getString("bookshop_item");
                jComboCategory.addItem(str);
            }
            ps.close();
            rs.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

