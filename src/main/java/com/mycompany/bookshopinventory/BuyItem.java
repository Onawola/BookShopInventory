    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed
        double myprice = 0;
        Integer quantity = 0;
        try {
            quantity = Integer.parseInt(jTxtQuantity.getText().trim());
            myprice = quantity * price;
            if (quantity <= totalQuantity) {
                try {
                    int newQuantity = totalQuantity - quantity;
                    conn = DriverManager.getConnection(Constants.url, Constants.user, Constants.password);
                    ps = conn.prepareStatement(INSERT_EXPENDITURE_SQL);
                    ps.setString(1, username);
                    ps.setString(2, category);
                    ps.setInt(3, quantity);
                    ps.setDouble(4, myprice);
                    ps.executeUpdate();
                    ps = conn.prepareStatement(UPDATE_STOCK_SQL);
                    ps.setInt(1, newQuantity);
                    ps.setString(2, category);
                    ps.executeUpdate();
                    ps.close();
                    conn.close();
                    JOptionPane.showMessageDialog(jBtnAdd, "Data Saved");
                    jTxtQuantity.setText("");
                    jBtnAdd.setEnabled(false);
                    jComboCategory.setSelectedIndex(0);
                    ps.close();
                    rs.close();
                    conn.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(jBtnAdd, "Out of Stock");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(jBtnAdd, "Enter a valid quantity");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jBtnAddActionPerformed

