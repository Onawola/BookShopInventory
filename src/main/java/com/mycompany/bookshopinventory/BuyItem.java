    private void jComboCategoryItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboCategoryItemStateChanged
        if (jComboCategory.getSelectedIndex() > 0) {
            category = jComboCategory.getSelectedItem().toString();
            jBtnAdd.setEnabled(true);
            try {
                conn = DriverManager.getConnection(Constants.url, Constants.user, Constants.password);
                ps = conn.prepareStatement(DISPLAY_ITEM_SQL);
                ps.setString(1, category);
                rs = ps.executeQuery();
                if (rs.next()) {
                    price = rs.getDouble("price");
                    totalQuantity = rs.getInt("quantity");
                    jTxtQuantity.setText("1");
                    jLabelPrice.setText(String.valueOf(price));
                }
                ps.close();
                rs.close();
                conn.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        } else {
            jBtnAdd.setEnabled(false);
            jTxtQuantity.setText("");
            jLabelPrice.setText("0");
        }
    }//GEN-LAST:event_jComboCategoryItemStateChanged

